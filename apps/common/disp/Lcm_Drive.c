u8 lcm_disp_audio(void)
{
    if(ui_disp.func_sel ==FUNC_SEL_NULL)
    	return FALSE;
    else if(ui_disp.func_sel ==FUNC_SEL_TIME)
    {
		lcm_disp_clock();
		return TRUE;
    }
    memset(lLcdStr,' ',16);
    Lcm_Disp(0,0,lLcdStr,4);
    Lcm_Disp(3,0,lLcdStr,4);
    
    lLcdStr[0]='S';
    lLcdStr[1]='E';
    lLcdStr[2]='T';
    Lcm_Disp_8_16(1,4,lLcdStr,4);
    
	lcm_full_y(0,4,32,1);

	switch(ui_disp.func_sel)
	{
		case FUNC_SEL_VOLUME:
            lcm_disp_null(0);
            lcm_disp_vol(get_cur_vol(),1);
            lcm_disp_bas(setup.aud_bass,3);
			break;
		case FUNC_SEL_BASS:
            lcm_disp_vol(get_cur_vol(),0);
            lcm_disp_bas(setup.aud_bass,1);
            lcm_disp_tre(setup.aud_treble,3);
			break;
		case FUNC_SEL_TREBLE:
            lcm_disp_bas(setup.aud_bass,0);
            lcm_disp_tre(setup.aud_treble,1);
            lcm_disp_bal(setup.aud_bal,3);
			break;
		case FUNC_SEL_BAL:
            lcm_disp_tre(setup.aud_treble,0);
            lcm_disp_bal(setup.aud_bal,1);
            lcm_disp_fad(setup.aud_fad,3);
			break;
		case FUNC_SEL_FAD:
            lcm_disp_bal(setup.aud_bal,0);
            lcm_disp_fad(setup.aud_fad,1);
            lcm_disp_loud(setup.aud_loud,3);
			break;
		case FUNC_SEL_LOUD:
            lcm_disp_fad(setup.aud_fad,0);
            lcm_disp_loud(setup.aud_loud,1);
            lcm_disp_eq(setup.aud_eq,3);
			break;
		case FUNC_SEL_EQ:
            lcm_disp_loud(setup.aud_loud,0);
            lcm_disp_eq(setup.aud_eq,1);
            lcm_disp_null(3);
			break;
		default:
			break;
	}
	return TRUE;
}
