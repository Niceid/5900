//x:0~3页  共4*8=32行
//y:0~127 共128列
//x_size 表示x占用的页数 取值1~4页
//y_size 表示y占用的列数 取值1:8列 2:16列
//data 表示字符串指针
//data_size表示有几个字符串
void Lcm_Disp(u8 x_start,u8 y_start,u8 *data,u8 data_size)
{
    u8 *p_disp_buf;
    y_start+=4;
    if((8*data_size) >128)
    {//y坐标越界 退出
        return;
    }
//	for(u8 col=x_start;col<1;col++)
	{//页数
        Lcm_Start();
        Lcm_Write(0x70); // set slave address
        Lcm_Write(0xb3-x_start);  //set page address
        Lcm_Write(0x10+(y_start>>4));//set column address
        Lcm_Write(0x00+(y_start&0x0f));//
        Lcm_Stop();
        Lcm_Start();
        Lcm_Write(0x72); // set slave address
		for(u8 y=0;y<data_size;y++)
		{
            if(data[y]>='a'&&data[y]<='z')
            {
                p_disp_buf=Digt_8_8buf[(data[y]-'a')+10];
            }
            else if(data[y]>='A'&&data[y]<='Z')
            {
                p_disp_buf=Digt_8_8buf[(data[y]-'A')+36];
            }
			else if(data[y]>=0&&data[y]<=9)
			{
				p_disp_buf=Digt_8_8buf[data[y]];
			}
			else if(data[y]>='0'&&data[y]<='9')
			{
				p_disp_buf=Digt_8_8buf[data[y]-'0'];
			}
            else
            {
                p_disp_buf=Digt_8_8buf[62];
                for(u8 i = 0;i < sizeof(spec_char_Index); i++)
                {
                    if(data[y] == spec_char_Index[i])
                    {
                        p_disp_buf=Digt_8_8buf[i+62];
                        break;
                    }
                }
            }
            for(u8 y_8bit=0;y_8bit<8;y_8bit++)
            {
				Lcm_Write(p_disp_buf[y_8bit]);
			}
		}
        for(u8 i=(y_start+data_size*8);i<132;i++)
        {
            Lcm_Write(0x00);
        }
        Lcm_Stop();
	}
}
