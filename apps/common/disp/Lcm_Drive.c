void Lcm_Write (u8 data)
{
#if defined(cmd_data_writer_together)
	if(writer_cmd_data_falg==0x01)//写命令
	{
		wcomspi(data);
	}
	else if(writer_cmd_data_falg==0x02)//写数据
	{
		wdataspi(data);
	}
	if(writer_cmd_data_falg==0x00)
	{
		if(data==0x70)
		{
			writer_cmd_data_falg=0x01;//写命令
		}
		else if(data==0x72)
		{
			writer_cmd_data_falg=0x02;//写数据
		}
	}
#else
	u8 k,i=25;
    LCM_SDA_OUT();//数据脚做输出
	for (k=0; k<8; k++)
	{
        if(data&0x80)
        {
            LCM_SDA_HIGH();//数据高
        }
        else
        {
            LCM_SDA_LOW();//数据低
        }
        Lcm_delay(50);//延时5us
        LCM_SCL_HIGH();//时钟高
        Lcm_delay(50);//延时5us
		data=data<<1;//数据移位
        LCM_SCL_LOW();//时钟低
	}

    LCM_SDA_IN();//数据置为输入
    Lcm_delay(50);//延时5us
    LCM_SCL_HIGH();//时钟高
    Lcm_delay(50);//延时5us
    while (LCM_SDA_GET() && i--);
    LCM_SDA_OUT();//数据脚做输出
    LCM_SCL_LOW();//时钟低
#endif
}
