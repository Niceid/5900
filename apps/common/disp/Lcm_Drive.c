void graphic(int *pic)
{
	 int i,page;

	 for(page=0;page<4;page++)
	 {
	  wcomspi(0x10);//列起始(高位)
	  wcomspi(0x02);//列起始(低位)
	  wcomspi(page+0xb0);//行起始
	   for(i=0;i<128;i++)
	    {wdataspi(*pic);pic++;}
	 }
}

