//初始化
void ini_s6b0724(void)
{
    wcomspi(0xE2);         //initialize interal function
    wcomspi(0x2F);         //power control(VB,VR,VF=1,1,1)
    wcomspi(0x24);         //Regulator resistor select
    wcomspi(0xA2);         //set LCD bias=1/9
    wcomspi(0x81);         //set reference voltage
    wcomspi(0x1F);         //VOP  V0=RRX[(99+EV)/162]X2.1  微调对比度
   
    wcomspi(0xC8);         //set SHL COM1 to COM64
    //wcomspi(0xA0);         //ADC select SEG1 to SEG132
    //wcomspi(0xC0);         //set SHL COM1 to COM64
    wcomspi(0xA1);         //ADC select SEG1 to SEG132
   
    wcomspi(0x40);         //Initial Display Line
   
    //wcomspi(0xA6);         //set reverse display OFF  //亮点显示
    wcomspi(0xA7);         //set reverse display OFF         //黑点显示
    wcomspi(0xB0);        // Set page address    页地址
	wcomspi(0x10);        // Set High Column Address  列地址高4位
	wcomspi(0x00);        // Set Low Column Address   列地址高4位
    wcomspi(0xAF);         //turns the display ON  AF

}
