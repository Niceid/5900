//写指令
void wcomspi(int cmd)
{
	int i;

	DOT_CS_LOW();
	DOT_AO_LOW();
	for(i=0;i<8;i++)
	{
		LCM_SCL_LOW();
		delay(5);
	 	if(cmd&0x80)
	 	{
			
	 		LCM_SDA_HIGH();
	 	}
		else
		{
			
		 	LCM_SDA_LOW();
		} 
		LCM_SCL_HIGH();
		delay(5);
		cmd=cmd<<1;
	}
	DOT_CS_HIGH();
}

//写数据
void wdataspi(int dat)
{
	int i;
	
	DOT_CS_LOW();
	DOT_AO_HIGH();
	for(i=0;i<8;i++)
	{
		LCM_SCL_LOW();
		delay(5);
		if(dat&0x80)
		{
			
			LCM_SDA_HIGH();
		}
		else
		{
			
			LCM_SDA_LOW();
		} 
		LCM_SCL_HIGH();
		delay(5);
		dat=dat<<1;
	}
	DOT_CS_HIGH();
}


