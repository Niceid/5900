{
	u8 col;
	Lcm_Start();
	Lcm_Write(0x70); // set slave address
	Lcm_Write(0xb3-page);  //set page address
	Lcm_Write(0x10);//set column address
	Lcm_Write(0x00);//
	Lcm_Stop();
	Lcm_Start();
	Lcm_Write(0x72); // set slave address
	for(col=0;col<131;col++)
	{
		Lcm_Write(0x00);
	}
	Lcm_Stop();
}

