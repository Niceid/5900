static uint8 TEA685x_Patch_Load(const uint8 *pLutBytes, uint16 size)
{
#define TEF665X_SPLIT_SIZE 			24

	uint8 buf[TEF665X_SPLIT_SIZE + 1];
	uint16 i,len;
	uint8 r;

	buf[0] = 0x1b;

	while(size)
	{
		len = (size > TEF665X_SPLIT_SIZE) ? TEF665X_SPLIT_SIZE : size;
		size -= len;

		for(i = 0; i < len; i++)
		{
			buf[1+i] = pLutBytes[i];
		}

		pLutBytes += len;

		r = Write_TEA685x_RegMap(buf, len+1);

		if(!r)
		{
			break;
		}
	}

	//ew_printf("r:%d\n",r);
	return r;
}

static uint8 TEA685x_Table_Write(const uint8 *tab)
{
//use a timer delay in init table
	//ew_puts("TEA685x_Table_Write\n");
	if(tab[1] == INIT_FLAG_TIMER)
	{
		//ew_puts("TEA685x_Table_Write111\n");
		OSTimeDly(tab[2]/10);
		return TRUE;
	}
//load patch table1
	else if(tab[1] == INIT_FLAG_PATCH1)
	{
		//ew_puts("TEA685x_Table_Write222\n");
		return TEA685x_Patch_Load(PatchByteValues, sizeof(PatchByteValues));
	}
//load patch table2
	else if(tab[1] == INIT_FLAG_PATCH2)
	{
		//ew_puts("TEA685x_Table_Write333\n");
		return TEA685x_Patch_Load(LutByteValues, sizeof(LutByteValues));
	}
	else
	{
		//ew_puts("TEA685x_Table_Write444\n");
		return Write_TEA685x_RegMap((uint8 *)&tab[1], tab[0]);
	}
}

static uint8 TEA685x_Boot(void)
{
	uint16 i;
	const uint8 *p = TUNER_INI_PARA;

	ew_puts("TEA685x_Boot\n");
	for(i = 0; i < sizeof(TUNER_INI_PARA); i += (p[i]+1))
	{
		if(!TEA685x_Table_Write(p+i))return FALSE;
	}

	return TRUE;
}

