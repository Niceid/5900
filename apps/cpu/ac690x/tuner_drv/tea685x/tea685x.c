static uint8 tuner_tea685x_init(radio_band_e band, uint16 min_freq, uint16 max_freq, uint16 freq)
{
	tuner_printf("%s, band:%d, min_freq:%d, max_freq:%d, freq:%d\n", __FUNCTION__, band, min_freq, max_freq, freq);

	if(tea685x_ctrl == NULL)
	{
		tea685x_ctrl = (tea685x_ctrl_t *)malloc_fun(tea685x_ctrl, sizeof(tea685x_ctrl_t), 0);
		if(tea685x_ctrl == NULL)
		{
			return FALSE;
		}
	}

	tuner_puts("tea685x_memory_ok\n");

	switch(band)
	{
		case RADIO_BAND_FM:
			if(min_freq > max_freq)goto tea685x_init_error;
			if(min_freq < 6400)goto tea685x_init_error;
			if(max_freq > 10800)goto tea685x_init_error;
			tea685x_ctrl->TunerBand = TEA685x_TUNER_BAND_REC_FM;
			break;

		case RADIO_BAND_AM:
			if(min_freq > max_freq)goto tea685x_init_error;
			if(min_freq < 520)goto tea685x_init_error;
			if(max_freq > 1710)goto tea685x_init_error;
			tea685x_ctrl->TunerBand = TEA685x_TUNER_BAND_REC_MW;
			break;

		default:
			goto tea685x_init_error;
			break;
	}

	if(tea685x_ctrl->band == band)
	{
		if(tea685x_ctrl->init_succeed)
		{
			if(tea685x_ctrl->now_freq == freq)
			{
				tea685x_ctrl->min_freq = min_freq;
				tea685x_ctrl->max_freq = max_freq;
				if(!tuner_tea685x_setting(TUR_SET_MUTE, OFF))goto tea685x_init_error;
				return TRUE;
			}
			else
			{
				goto tea685x_init_freq;
			}
		}
		else
		{
			goto tea685x_init_error;
		}
	}

	if(!tea685x_set_init())goto tea685x_init_error;
	tuner_puts("tea685x_set_init1234\n");

tea685x_init_freq:
	tea685x_ctrl->band = band;
	tea685x_ctrl->min_freq = min_freq;
	tea685x_ctrl->max_freq = max_freq;
	tea685x_ctrl->init_succeed = TRUE;

	engine_status_t status;
	if(!tuner_tea685x_search(freq, &status))goto tea685x_init_error;
	if(!tuner_tea685x_setting(TUR_SET_MUTE, OFF))goto tea685x_init_error;
	tuner_puts("TEA685x Init Succeed!!!!!\n");
	return TRUE;

tea685x_init_error:
	free_fun((void **)&tea685x_ctrl);
	tuner_puts("TEA685x Init Fail!!!!!\n");
	return FALSE;
}

