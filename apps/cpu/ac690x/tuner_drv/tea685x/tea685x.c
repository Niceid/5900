	if(tea685x_ctrl == NULL)return FALSE;
	if(!tea685x_ctrl->init_succeed)return FALSE;
	if(freq < tea685x_ctrl->min_freq)return FALSE;
	if(freq > tea685x_ctrl->max_freq)return FALSE;

	tuner_printf("%s, freq:%d\n", __FUNCTION__, freq);

	//tuner_printf(" tuner_tea685x_freq mode:%d\n", GET_RADIO_STATE);

	if(!tea685x_set_freq(Radio_PRESETMODE, freq))return FALSE;

