static uint8 TEA685x_SetFreq(uint8 mode, uint16 Freq)
{
	TEA685x_TUNING_ACTION_e A2Mode;

	switch(mode)
	{
		case Radio_PRESETMODE:
			A2Mode = TEA685x_TUNINGACTION_PRESET;
			break;
		case Radio_SEARCHMODE:
			A2Mode = TEA685x_TUNINGACTION_SEARCH;
			break;
		case Radio_AFUPDATEMODE:
			A2Mode = TEA685x_TUNINGACTION_AF_UPDATE;
			break;
		case Radio_JUMPMODE:
			A2Mode = TEA685x_TUNINGACTION_JUMP;
			break;
		case Radio_CHECKMODE:
			A2Mode = TEA685x_TUNINGACTION_CHECK;
			break;
		default:
			return FALSE;
	}

	return TEA685x_Tune_To(A2Mode, Freq);
}

