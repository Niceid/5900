static uint8 Write_TEA685x_RegMap(const uint8 *value, uint16 length)
{
	if(!length)return TRUE;

	i2c_data_init(TEA685x_I2C_ADDR, TEA685x_I2C_RATE, &radio_gpio);
	uint8 result = i2c_data_w(value, length);
	i2c_data_exit();

	return result;
}

static uint8 Read_TEA685x_RegMap(uint8 *value, uint16 length)
{
	if(!length)return TRUE;

	i2c_data_init(TEA685x_I2C_ADDR, TEA685x_I2C_RATE, &radio_gpio);
	uint8 result = i2c_data_r(value, length);
	i2c_data_exit();

	return result;
}

static uint8 check_detect_tea685x(void)
{
	i2c_data_init(TEA685x_I2C_ADDR, TEA685x_I2C_RATE, &radio_gpio);
	uint8 result = i2c_data_w(NULL, 0);
	i2c_data_exit();

	return result;
}

static uint8 dev_tea685x_Get_Cmd(TEA685X_MODULE_e module, uint8 cmd, uint8 *receive, uint16 length)
{
	uint8 buf[3];
	uint8 r;

	buf[0]= module;			//module,	FM/AM/APP
	buf[1]= cmd;			//cmd,		1,2,10,...
	buf[2]= 1;				//index, 		always 1

	if(!Write_TEA685x_RegMap(buf, 3))return FALSE;
	r = Read_TEA685x_RegMap(receive, length);

	return r;
}

static uint8 dev_tea685x_Set_Cmd(TEA685X_MODULE_e module, uint8 cmd, int length,...)
{
	uint8 buf[30];
	int temp;
	uint16 i;
	uint8 r;

	va_list vArgs;
    va_start(vArgs, length);

	buf[0]= module;		//module,	FM/AM/APP
	buf[1]= cmd;		//cmd,		1,2,10,...
	buf[2]= 1;			//index, 		always 1

	for(i = 3;i < length; i+=2)
	{
		temp = va_arg(vArgs, int);	//the size only int valid for compile

		buf[i]  = (temp >> 8) & 0xFF;
		buf[i+1]= (temp & 0xFF);
	}

	va_end(vArgs);

	r = Write_TEA685x_RegMap(buf, length);

	return r;
}

