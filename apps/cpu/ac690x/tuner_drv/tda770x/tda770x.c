static uint8 tda770x_cmd_get_seekstatus(tda770x_band_e band,uint32 *get_freq,uint8 *get_station,uint16 *get_signal)
{
	uint16 total = 8;
	uint16 i;
	uint16 smeter = 0;
	uint16 detuning = 0;
	uint16 multipath = 0;
	uint16 adj_channel = 0;
	
	for(i = 0; i < total; i++)
	{
		tda770x_write_addr[0] = 0x00;
		tda770x_write_addr[1] = 0x00;
		if(i == (total - 1))
		{
			tda770x_write_addr[2] = SeekStatus_RstQaul;
		}
		else
		{
			tda770x_write_addr[2] = SeekStatus_NoRstQaul;
		}
		if(!tda770x_cmd_write_read(CmdCode_ReadSeekStatus, 1, 1+2+2+1))return FALSE;
		
		smeter		+= tda770x_read_addr[12];
		detuning	+= tda770x_read_addr[14];
		multipath	+= tda770x_read_addr[9];
		adj_channel += tda770x_read_addr[10];
	}

	uint8 deviation	= tda770x_read_addr[11];
	uint32 freq = _u8tou32_(0x00, tda770x_read_addr[6], tda770x_read_addr[7], tda770x_read_addr[8]);

	if(band == TDA770x_BAND_FM)
	{
		*get_freq = _u8tou32_(0x00, tda770x_read_addr[6], tda770x_read_addr[7], tda770x_read_addr[8]);
		*get_freq 	/= 10;

		freq /= 10;
	}
	else if(band == TDA770x_BAND_AM)
	{
		*get_freq = _u8tou32_(0x00, tda770x_read_addr[6], tda770x_read_addr[7], tda770x_read_addr[8]);
	}
	
	smeter 		/= total;
	multipath 	/= total;
	adj_channel	/= total;
	//detuning;
	
	uint16 acp;
	if(smeter < FSscalingthrmin)
	{
		acp = adj_channel;
	}
	else if(smeter > FSscalingthrmax)
	{
		acp = adj_channel*6/10;
	}
	else
	{
		acp = (adj_channel - adj_channel*6/10)*(FSscalingthrmax - smeter)/(FSscalingthrmax - FSscalingthrmin) + (adj_channel*6/10);
	}
	
	ew_printf("smeter:%d,acp:%d,detuning:%d\n",smeter,acp,detuning);
	
	*get_signal = smeter;
	if(band == TDA770x_BAND_FM)
	{
		if((smeter >= TDA770x_TUNER_FM_TUNE_RSSI )
		&&(((acp<=80)&&(detuning<=40)) || ((acp<95)&&(detuning<=25))))
		{
			if(((freq%10) != 0)&&((deviation == 0)||(adj_channel >= 0x13)))
			{//	50K
				*get_station = 0;
			}
			else
			{
				*get_station = 1;
			}
		}
	}
	else if(band == TDA770x_BAND_AM)
	{
		if((smeter>=TDA770x_TUNER_AM_TUNE_RSSI) && (detuning<12*total))
		{
			*get_station = 1;
		}
	}
	else return FALSE;
	
	return TRUE;
}

