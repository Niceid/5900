static uint8 tda770x_cmd_set_becoeff(const uint8 *p_be_coeff)
{
	uint8 num,num_word;
	uint8 i;
	uint32 addr;
	uint8 cmdcode;
	uint8 offset;
	
	while(*p_be_coeff != 0xff)
	{
		num = num_word = p_be_coeff[0];
		addr = _u8tou32_(0x00, p_be_coeff[1], p_be_coeff[2], p_be_coeff[3]);
		offset = 0;
		
		if((addr <= 0x019fff)||((addr >= 0x01c000)&&(addr <= 0x01ffff)))
		{
			cmdcode = CmdCode_WriteDMAmem;
		}
		else if((addr >= 0x01a000)&&(addr <= 0x01afff))
		{
			cmdcode = CmdCode_WriteXmen;
		}
		else if((addr >= 0x01b000)&&(addr <= 0x01bfff))
		{
			cmdcode = CmdCode_WriteYmen;
		}
		
		if(cmdcode == CmdCode_WriteDMAmem)
		{
			for(i = 0; i < num; i++)
			{
				tda770x_write_addr[0] = _u32to8hl_(addr);		//Para 1  memory address
				tda770x_write_addr[1] = _u32to8lh_(addr);
				tda770x_write_addr[2] = _u32to8ll_(addr);
				tda770x_write_addr[3] = p_be_coeff[4 + i*4];	//Para 2
				tda770x_write_addr[4] = p_be_coeff[5 + i*4];
				tda770x_write_addr[5] = p_be_coeff[6 + i*4];
				tda770x_write_addr[6] = tda770x_write_addr[4];
				tda770x_write_addr[7] = tda770x_write_addr[5];
				tda770x_write_addr[8] = p_be_coeff[7 + i*4];
				
				if(!tda770x_cmd_write_read(cmdcode, 3, 1))return FALSE;
				
				addr++;
			}
			
			p_be_coeff += num_word*4 + 4;
		}
		else if((cmdcode == CmdCode_WriteXmen)||(cmdcode == CmdCode_WriteYmen))
		{
			while(num > 7)
			{
				tda770x_write_addr[0] = _u32to8hl_(addr);		//Para 1  memory address
				tda770x_write_addr[1] = _u32to8lh_(addr);
				tda770x_write_addr[2] = _u32to8ll_(addr);
				
				for(i = 0; i < 7; i++)
				{
					tda770x_write_addr[3 + i*3] = p_be_coeff[4 + i*4 + offset];//Para 2
					tda770x_write_addr[4 + i*3] = p_be_coeff[5 + i*4 + offset];
					tda770x_write_addr[5 + i*3] = p_be_coeff[6 + i*4 + offset];
				}
				
				if(!tda770x_cmd_write_read(cmdcode, 8, 1))return FALSE;
				
				addr++;
				offset += 4*7;
				num -= 7;
			}
			
			tda770x_write_addr[0] = _u32to8hl_(addr);		//Para 1  memory address
			tda770x_write_addr[1] = _u32to8lh_(addr);
			tda770x_write_addr[2] = _u32to8ll_(addr);
			
			for(i = 0; i < num; i++)
			{
				tda770x_write_addr[3 + i*3] = p_be_coeff[4 + i*4 + offset];//Para 2
				tda770x_write_addr[4 + i*3] = p_be_coeff[5 + i*4 + offset];
				tda770x_write_addr[5 + i*3] = p_be_coeff[6 + i*4 + offset];
			}
			
			if(!tda770x_cmd_write_read(cmdcode, num+1, 1))return FALSE;
			
			p_be_coeff += num_word*4 + 4;
		}
	}
	
	return TRUE;
}

