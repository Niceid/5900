static void delay_1us(uint8 count)
{
	uint8 i,j;
	for(i = 0;i < count; i++)
	{
		for(j = 0; j < 10; j++)
		{
			asm volatile ("nop");
		}
	}
}


static uint8 Write_TDA770x_RegMap(const uint8 *value, uint16 length)
{
	if(!length)return TRUE;
	
	i2c_data_init(TDA770x_I2C_ADDR, TDA770x_I2C_RATE, &radio_gpio);
	uint8 result = i2c_data_w(value, length);
	i2c_data_exit();
	
	return result;
}

static uint8 Read_TDA770x_RegMap(uint8 *value, uint16 length)
{
	if(!length)return TRUE;
	
	i2c_data_init(TDA770x_I2C_ADDR, TDA770x_I2C_RATE, &radio_gpio);
	uint8 result = i2c_data_r(value, length);
	i2c_data_exit();
	
	return result;
}

