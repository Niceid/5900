static uint8 tuner_tda770x_init(radio_band_e band, uint16 min_freq, uint16 max_freq, uint16 freq)
{				
	ew_printf("%s, band:%d, min_freq:%d, max_freq:%d, freq:%d\n", __FUNCTION__, band, min_freq, max_freq, freq);
	
	if(tda770x_ctrl == NULL)
	{
		tda770x_ctrl = (tda770x_ctrl_t *)malloc_fun(tda770x_ctrl, sizeof(tda770x_ctrl_t), 0);
		if(tda770x_ctrl == NULL)
		{
			return FALSE;
		}
		tda770x_ctrl->area = TDA770x_AREA_EUROPE;
	}
	
	ew_puts("tda770x_memory_ok\n");
	
	switch(band)
	{
		case RADIO_BAND_FM:
			if(min_freq > max_freq)goto tda770x_init_error;
			if(min_freq < 6400)goto tda770x_init_error;
			if(max_freq > 10800)goto tda770x_init_error;
			tda770x_ctrl->TunerBand = TDA770x_TUNER_BAND_REC_FM;
			break;
			
		case RADIO_BAND_AM:
			if(min_freq > max_freq)goto tda770x_init_error;
			if(min_freq < 520)goto tda770x_init_error;
			if(max_freq > 1710)goto tda770x_init_error;
			tda770x_ctrl->TunerBand = TDA770x_TUNER_BAND_REC_MW;
			break;
			
		default:
			goto tda770x_init_error;
			break;
	}
	
	if(tda770x_ctrl->band == band)
	{
		if(tda770x_ctrl->init_succeed)
		{
			if(tda770x_ctrl->now_freq == freq)
			{
				tda770x_ctrl->min_freq = min_freq;
				tda770x_ctrl->max_freq = max_freq;
				if(!tuner_tda770x_setting(TUR_SET_MUTE, OFF))goto tda770x_init_error;
				return TRUE;
			}
			else
			{
				goto tda770x_init_freq;
			}
		}
		else 
		{
			goto tda770x_init_error;
		}
	}
	
	if(!tda770x_set_init())goto tda770x_init_error;
	
tda770x_init_freq:
	ew_puts("tda770x_init_freq\n");
	if(!tda770x_change_band(tda770x_get_band(), min_freq, max_freq))return FALSE;
	tda770x_ctrl->band = band;
	tda770x_ctrl->min_freq = min_freq;
	tda770x_ctrl->max_freq = max_freq;
	tda770x_ctrl->init_succeed = TRUE;
	
	engine_status_t status;
	if(!tuner_tda770x_search(freq, &status))goto tda770x_init_error;
	OSTimeDly(1);
	if(!tuner_tda770x_search(freq, &status))goto tda770x_init_error;
	if(!tuner_tda770x_setting(TUR_SET_MUTE, OFF))goto tda770x_init_error;
	ew_puts("TDA770x Init Succeed!!!!!\n");
	return TRUE;
	
tda770x_init_error:
	free_fun((void **)&tda770x_ctrl);
	ew_puts("TDA770x Init Fail!!!!!\n");
	return FALSE;
}

