static uint8  tda770x_cmd_get_TDSR(uint8 *AlignDone, uint8 *IS, uint8 *TDBusy)
{
	if(!tda770x_cmd_write_read(CmdCode_ReadTDS, 0, 1+1+1))return FALSE;	//add read out checksum
	
	if(tda770x_read_addr[4] & 0x04)
	{
		*AlignDone = 1;
	}
	else
	{
		*AlignDone = 0;
	}
	
	if(tda770x_read_addr[4] & 0x08)
	{
		*IS = 1;
	}
	else
	{
		*IS = 0;
	}
	
	if(tda770x_read_addr[5] & 0x40)
	{
		*TDBusy = 1;
	}
	else
	{
		*TDBusy = 0;
	}
	
	return TRUE;
}

