static uint8 tda770x_check_ESD_crashed(void)
{
	uint8 read_smeter_log[3];
	uint8 read_sysco_reg1[3];
	uint8 k1 = 0,k2 = 0,k3 = 0,k4 = 0;
	
	tda770x_ctrl->cmd_check = TRUE;
	
	if(!tda770x_cmd_get_mem(0x01a0f9, 1))//FM_Smeter(log)
	{
		k4 = 1;
	}
	read_smeter_log[0] = tda770x_read_addr[3]; 
	read_smeter_log[1] = tda770x_read_addr[4]; 
	read_smeter_log[2] = tda770x_read_addr[5];
	
	if(!tda770x_cmd_get_mem(0x014001, 1))//SYSCO_REG1
	{
		k4 = 1;
	}
	read_sysco_reg1[0] = tda770x_read_addr[3]; 
	read_sysco_reg1[1] = tda770x_read_addr[4];
	read_sysco_reg1[2] = tda770x_read_addr[5];
	
	tda770x_ctrl->cmd_check = FALSE;
	
	if((read_sysco_reg1[1] != 0x02)&&(read_sysco_reg1[2] != 0x7B))      //TDA7703=0X00027B,TDA7706M=0X80027B
	{
		k1 = 1;
	}
	if((read_sysco_reg1[1] == read_smeter_log[1])&&(read_sysco_reg1[2] == read_smeter_log[2]))
	{
		k2 = 1;
	}
	if((read_smeter_log[0] == 0)&&(read_smeter_log[1] == 0)&&(read_smeter_log[2] == 0)) 
	{
		k3 = 1;
	}
	
	if((k1==1)||(k2==1)||(k3==1)||(k4==1))   
	{
		return FALSE;
	}
	
	return TRUE;
}

