static uint8 tuner_tda770x_freq(uint16 freq)
{	
	if(tda770x_ctrl == NULL)return FALSE;
	if(!tda770x_ctrl->init_succeed)return FALSE;
	if(freq < tda770x_ctrl->min_freq)return FALSE;
	if(freq > tda770x_ctrl->max_freq)return FALSE;
	
	ew_printf("%s, freq:%d\n", __FUNCTION__, freq);
	
	if(!tda770x_set_freq(tda770x_get_band(), freq))return FALSE;
		
	tda770x_ctrl->now_freq = freq;
	tda770x_ctrl->signal = 0;
	tda770x_ctrl->last_sys_timer = g_rand_seed;
	return TRUE;
}

static uint8 tuner_tda770x_status(engine_status_t *status)
{
	if(tda770x_ctrl == NULL)return FALSE;
	if(!tda770x_ctrl->init_succeed)return FALSE;
		
	uint8 fm_mode 	= (tda770x_ctrl->TunerBand == TDA770x_TUNER_BAND_REC_FM) ? 1 : 0;
	status->band 	= fm_mode ? RADIO_BAND_FM : RADIO_BAND_AM;
	status->freq 	= tda770x_ctrl->now_freq;
	status->signal 	= tda770x_ctrl->signal;
	status->stc 	= FALSE;
	status->valid   = FALSE;
	status->stereo 	= FALSE;
	
	if(tda770x_ctrl->TunerAutoSeek)
	{
		uint8  station = 0;
		uint16 signal = 0;
		uint32 get_freq = 0;
		
		OSTimeDly(5);
		if(tda770x_cmd_get_seekstatus(tda770x_get_band(), &get_freq, &station, &signal))
		{
			tda770x_ctrl->now_freq	= get_freq;
			tda770x_ctrl->station	= station;
			tda770x_ctrl->signal = signal;
				
			//ew_printf("tda770x_ctrl->now_freq:%d\n",tda770x_ctrl->now_freq);
			//ew_printf("tda770x_ctrl->station:%d\n",tda770x_ctrl->station);
			if(tda770x_ctrl->station)
			{
				if(!tda770x_cmd_set_seekend(SeekStopUnmuted))return FALSE;
				status->stc = TRUE;
			}
			else
			{
				if(tda770x_cmd_set_seekend(SeekContinue))
				{
					tda770x_ctrl->station = FALSE;
					status->stc = FALSE;
				}
			}
			status->valid = tda770x_ctrl->station;
			status->freq  = tda770x_ctrl->now_freq;
			status->signal= tda770x_ctrl->signal;
			
		}
	}
	else
	{
		OSTimeDly(5);
		if(!tda770x_cmd_get_smeter(tda770x_get_band(), &tda770x_ctrl->signal, &tda770x_ctrl->detun, &tda770x_ctrl->dummy))return FALSE;
		status->signal	= tda770x_ctrl->signal;
		status->stc = TRUE;
		
		//ew_printf("status->signal:%d\n",status->signal);
		//ew_printf("tda770x_ctrl->dummy:%d\n",tda770x_ctrl->dummy);
		//ew_printf("tda770x_ctrl->detun:%d\n",tda770x_ctrl->detun);
		if(fm_mode)
		{
			if((((status->signal >= TDA770x_TUNER_FM_TUNE_RSSI)&&(tda770x_ctrl->now_freq != 10800))
				||((status->signal >= TDA770x_TUNER_FM_TUNE_RSSI+4)&&(tda770x_ctrl->now_freq == 10800)))
				&&((tda770x_ctrl->dummy < 3)
				&&(tda770x_ctrl->detun > 40)))
			{
				status->valid = TRUE;
			}
			
			if(status->signal > TDA770x_TUNER_FM_TUNE_RSSI)
			{
				uint8 stereo = FALSE;
				if(!tda770x_cmd_get_stereo(&stereo))return FALSE;
				if(stereo)
				{
					status->stereo = TRUE;
				}
			}
		}
	}
	
	return TRUE;
}

static uint8 tuner_tda770x_search(uint16 freq, engine_status_t *status)
{
	ew_printf("%s, freq:%d\n", __FUNCTION__, freq);
	
	if(tda770x_ctrl == NULL)return FALSE;
	if(!tda770x_ctrl->init_succeed)return FALSE;
	if(freq < tda770x_ctrl->min_freq)return FALSE;
	if(freq > tda770x_ctrl->max_freq)return FALSE;

	if(!tuner_tda770x_freq(freq))return FALSE;
	OSTimeDly(5);
	
	uint8 time_over = 40;
	do{
		OSTimeDly(1);
		if(!tuner_tda770x_status(status))return FALSE;
	}while(!status->stc && (--time_over));
	
	return TRUE;
}

static uint8 tuner_tda770x_hard(engine_hard_t *hard)
{
	if(tda770x_ctrl == NULL)return FALSE;
	if(!tda770x_ctrl->init_succeed)return FALSE;
	if(hard->enable)
	{
		engine_hard_start_t *start = &hard->ctrl.start;
		
		if(start->min_freq < tda770x_ctrl->min_freq)return FALSE;
		if(start->max_freq > tda770x_ctrl->max_freq)return FALSE;	
		if(start->start_freq < start->min_freq)return FALSE;
		if(start->start_freq > start->max_freq)return FALSE;		
		
		if(!tda770x_reset_autoseek())return FALSE;
		
		if(start->start_freq != tda770x_ctrl->now_freq)
		{
			if(!tda770x_set_freq(tda770x_get_band(),start->start_freq))return FALSE;
		}
		
		if(!tda770x_seek_on(tda770x_get_band(), (hard->ctrl.start.dir == UP)?
			((int)hard->ctrl.start.step):-((int) hard->ctrl.start.step))) return FALSE;
		
		OSTimeDly(20);
	}
	else
	{
		engine_hard_stop_t *stop = &hard->ctrl.stop;
		if(stop->stop_freq == 0xFFFF)
		{
			if(!tda770x_reset_autoseek())return FALSE;
		}
		else
		{
			if(stop->stop_freq < tda770x_ctrl->min_freq)return FALSE;
			if(stop->stop_freq > tda770x_ctrl->max_freq)return FALSE;
			if(!tda770x_reset_autoseek())return FALSE;
		}
		ew_puts("tuner_tda770x_hard--->stop succeed\n");
	}
	return TRUE;
}

static uint8 tuner_tda770x_setting(fm_setting_e type, void *set)
{
	if(tda770x_ctrl == NULL)return FALSE;
	if(!tda770x_ctrl->init_succeed)return FALSE;
	
	switch(type)
	{
		case TUR_SET_MUTE:
			{
				uint8 mute = (uint8)set;
				if(!tda770x_set_mute(mute))return FALSE;
			}
			break;
			
		case TUR_SET_STEREO:
			{
				uint8 stereo = (uint8)set;
				if(tda770x_ctrl->TunerBand == TDA770x_TUNER_BAND_REC_FM)
				{
					if(!tda770x_set_stereo(stereo))return FALSE;
				}
				else return FALSE;
			}
			break;
	
		default:
			return FALSE;
	}
	return TRUE;
}

static uint8 tuner_tda770x_info(fm_info_e type, void *info)
{	
	switch(type)
	{
		case TUR_INFO_DRV:
			if(info != NULL)
			{
				*(uint8 *)info = TUN_DRV_TDA770x;
			}
			break;
			
		case TUR_INFO_FM_RSSI:
			if(info != NULL)
			{
				*(uint8 *)info = TDA770x_TUNER_FM_TUNE_RSSI;
			}
			break;
			
		case TUR_INFO_FM_LOC_RSSI:
			if(info != NULL)
			{
				*(uint8 *)info = TDA770x_TUNER_FM_LOC_TUNE_RSSI;
			}
			break;

		case TUR_INFO_FM_DEAD_SPOT_LENGTH:
			if(info != NULL)
			{
				*(uint8 *)info = sizeof(fm_tda770x_deal_spot_tab)/sizeof(uint16);
			}
			break;
			
		case TUR_INFO_FM_DEAD_SPOT_TAB:
			if(info != NULL)
			{
				*(int *)info = (int)fm_tda770x_deal_spot_tab;
			}
			break;
		
		case TUR_INFO_AM_RSSI:
			if(info != NULL)
			{
				*(uint8 *)info = TDA770x_TUNER_AM_TUNE_RSSI;
			}
			break;
			
		case TUR_INFO_AM_LOC_RSSI:
			if(info != NULL)
			{
				*(uint8 *)info = TDA770x_TUNER_AM_LOC_TUNE_RSSI;
			}
			break;

		case TUR_INFO_AM_DEAD_SPOT_LENGTH:
			if(info != NULL)
			{
				*(uint8 *)info = sizeof(am_tda770x_deal_spot_tab)/sizeof(uint16);
			}
			break;
			
		case TUR_INFO_AM_DEAD_SPOT_TAB:
			if(info != NULL)
			{
				*(int *)info = (int)am_tda770x_deal_spot_tab;
			}
			break;
			
		case TUR_INFO_AUTOSEEK:
			if(info != NULL)
			{
				*(uint8 *)info = ENABLE;	//DISABLE
			}
			break;
						
		default:
			return FALSE;
	}
	return TRUE;
}

